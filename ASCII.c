// sum of ascii values

#include <stdio.h>

int main() {
    char c1 = 'A';
    char c2 = 'B';
    char c3 = 'C';

    int ascii1 = (int) c1;
    int ascii2 = (int) c2;
    int ascii3 = (int) c3;

    printf("The ASCII value of %c is %d\n", c1, ascii1);
    printf("The ASCII value of %c is %d\n", c2, ascii2);
    printf("The ASCII value of %c is %d\n", c3, ascii3);

    return 0;
}


